void start_ADCs (void) {
  HAL_ADC_Stop_DMA(&hadc1);
  HAL_ADC_Stop_DMA(&hadc4);
  
  pulse_tim8_ch2(1);
  HAL_Delay(20);
  
  HAL_ADC_Start_DMA(&hadc1, (uint32_t*)adc1_dma_buff, ADC_DMA_BUFFSIZE);
  HAL_ADC_Start_DMA(&hadc4, (uint32_t*)adc4_dma_buff, ADC_DMA_BUFFSIZE);
  
  pulse_tim8_ch2(1);
}
